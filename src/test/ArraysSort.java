
package test;


public class ArraysSort {
    public static void main(String[] args) {
        int [][] arr = {{12, 25, 9},{14,5, 8},{2, 8, 6}};
        
        for (int i = 0; i < arr.length; i++) {
            for (int j = 0; j < arr[i].length; j++) {
                System.out.println(arr[i][j]);
            }
            
        }
    }
}
